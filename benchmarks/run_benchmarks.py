#!/usr/bin/env python3
"""WareFleet benchmark harness.

Runs the same seeded order scenario across allocation strategies (and fleet
sizes), collects KPIs from the fleet manager's Prometheus endpoint, and writes:

  * a CSV per invocation into --out (benchmarks/results/, gitignored)
  * the results table into docs/results.md (between BENCH markers)

This is what turns the platform into a study: identical inputs, one variable
(the allocation strategy), measured outputs. H1 asks whether the idealized
ranking survives realistic execution — this produces the realistic half.

Usage:
    python3 benchmarks/run_benchmarks.py                          # 3 strategies x 2 robots
    python3 benchmarks/run_benchmarks.py --robots 2,4 --orders 8
    python3 benchmarks/run_benchmarks.py --dropout robot1:25:15   # H2: kill robot1 25s in, revive 15s later

Each run boots the full stack fresh (fair start, zeroed metrics), streams the
scenario, waits for completion, scrapes metrics, tears down.
"""
import argparse
import csv
import os
import subprocess
import sys
import time
import urllib.request
from datetime import datetime
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
METRICS_URL = "http://localhost:2112/metrics"
def bench_marks(tier: str):
    return (f"<!-- BENCH:{tier}:BEGIN (auto-generated by benchmarks/run_benchmarks.py) -->",
            f"<!-- BENCH:{tier}:END -->")

ROS_ENV = f"source /opt/ros/jazzy/setup.bash && source {ROOT}/ros2_ws/install/setup.bash"


def scrape_metrics():
    txt = urllib.request.urlopen(METRICS_URL, timeout=5).read().decode()
    out = {}
    for line in txt.splitlines():
        if line.startswith("warefleet_") and " " in line:
            k, v = line.rsplit(" ", 1)
            try:
                out[k] = float(v)
            except ValueError:
                pass
    return out


def start_stack(robots: int, alloc: str):
    env = dict(os.environ, ROBOTS=str(robots), WAREFLEET_ALLOCATION=alloc)
    r = subprocess.run(["bash", str(ROOT / "scripts/demo.sh"), "--headless"],
                       env=env, capture_output=True, text=True)
    if r.returncode != 0:
        print(r.stdout[-2000:], r.stderr[-1000:], file=sys.stderr)
        raise RuntimeError(f"stack failed to start (robots={robots}, alloc={alloc})")


def stop_stack():
    subprocess.run(["bash", str(ROOT / "scripts/stop.sh")], capture_output=True)


def kill_robot(name: str):
    subprocess.run(["pkill", "-f", f"robot_id:={name}"], capture_output=True)


def revive_robot(name: str):
    subprocess.run(["bash", str(ROOT / "scripts/demo.sh"), "agent-only", name],
                   capture_output=True)


def dump_schedule(scenario: str, orders: int) -> Path:
    """One schedule feeds both tiers — that's what makes H1 an honest comparison."""
    out = ROOT / "benchmarks/results/.schedule.json"
    out.parent.mkdir(parents=True, exist_ok=True)
    r = subprocess.run(
        ["bash", "-c",
         f"{ROS_ENV} && ros2 run warefleet_agent order_feeder "
         f"--scenario {scenario} --limit {orders} --dump {out}"],
        capture_output=True, text=True)
    if r.returncode != 0:
        raise RuntimeError(f"schedule dump failed: {r.stderr[-500:]}")
    return out


def run_one_idealized(schedule: Path, alloc: str, robots: int, orders: int,
                      rate: float, planner: str = "prioritized") -> dict:
    """One gridsim run (no physics, seconds not minutes)."""
    print(f"[bench] ── tier=idealized alloc={alloc} planner={planner} "
          f"robots={robots} orders={orders} rate={rate}x")
    gridsim = ROOT / "fleet_manager/bin/gridsim"
    r = subprocess.run(
        [str(gridsim), "-schedule", str(schedule), "-strategy", alloc,
         "-robots", str(robots), "-orders", str(orders), "-rate", str(rate),
         "-planner", planner],
        capture_output=True, text=True, timeout=120)
    if r.returncode != 0:
        raise RuntimeError(f"gridsim failed: {r.stderr[-500:]}")
    m = __import__("json").loads(r.stdout)
    row = {
        "tier": "idealized",
        "strategy": alloc,
        "planner": planner,
        "robots": robots,
        "orders": m["orders"],  # actual schedule size (scenario may cap below the ask)
        "completed": m["completed"],
        "wall_s": m["sim_s"],  # simulated seconds — the idealized clock
        "throughput_per_min": m["throughput_per_min"],
        "makespan_mean_s": m["makespan_mean_s"],
        "recoveries": 0,
        "recovery_mean_s": "",
    }
    print(f"[bench]    completed={row['completed']}/{orders} sim={row['wall_s']}s "
          f"throughput={row['throughput_per_min']}/min makespan={row['makespan_mean_s']}s")
    return row


def run_one(scenario: str, alloc: str, robots: int, orders: int, rate: float,
            dropout=None) -> dict:
    """One benchmark run; returns the KPI row."""
    label = f"alloc={alloc} robots={robots} orders={orders}" + (
        f" dropout={dropout}" if dropout else "")
    print(f"[bench] ── {label}")
    stop_stack()
    start_stack(robots, alloc)

    feeder = subprocess.Popen(
        ["bash", "-c",
         f"{ROS_ENV} && ros2 run warefleet_agent order_feeder "
         f"--scenario {scenario} --limit {orders} --rate {rate}"],
        stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)

    t0 = time.monotonic()
    killed_at = revived_at = None
    deadline = t0 + 120 * orders + 120
    completed = 0
    while time.monotonic() < deadline:
        time.sleep(3)
        elapsed = time.monotonic() - t0
        if dropout:
            name, kill_after, revive_after = dropout
            if killed_at is None and elapsed >= kill_after:
                print(f"[bench]    injecting dropout: killing {name} at t={elapsed:.0f}s")
                kill_robot(name)
                killed_at = elapsed
            if killed_at is not None and revived_at is None and \
                    elapsed >= killed_at + revive_after:
                print(f"[bench]    reviving {name} at t={elapsed:.0f}s")
                revive_robot(name)
                revived_at = elapsed
        try:
            m = scrape_metrics()
        except OSError:
            continue
        completed = int(m.get("warefleet_tasks_completed_total", 0))
        if completed >= orders:
            break
    wall = time.monotonic() - t0

    try:
        m = scrape_metrics()
    except OSError:
        m = {}
    feeder.terminate()
    stop_stack()

    mk_sum = m.get("warefleet_task_makespan_seconds_sum", 0.0)
    mk_cnt = m.get("warefleet_task_makespan_seconds_count", 0.0)
    rec_sum = m.get("warefleet_dropout_recovery_seconds_sum", 0.0)
    rec_cnt = m.get("warefleet_dropout_recovery_seconds_count", 0.0)
    row = {
        "tier": "realistic",
        "strategy": alloc,
        "planner": "nav2",  # execution via Nav2; MAPF-in-the-loop is future work
        "robots": robots,
        "orders": orders,
        "completed": completed,
        "wall_s": round(wall, 1),
        "throughput_per_min": round(completed / (wall / 60.0), 2) if wall else 0,
        "makespan_mean_s": round(mk_sum / mk_cnt, 1) if mk_cnt else "",
        "recoveries": int(rec_cnt),
        "recovery_mean_s": round(rec_sum / rec_cnt, 1) if rec_cnt else "",
    }
    print(f"[bench]    completed={row['completed']}/{orders} wall={row['wall_s']}s "
          f"throughput={row['throughput_per_min']}/min makespan={row['makespan_mean_s']}s")
    return row


def write_csv(rows, out_dir: Path) -> Path:
    out_dir.mkdir(parents=True, exist_ok=True)
    path = out_dir / f"bench_{datetime.now():%Y%m%d_%H%M%S}.csv"
    with open(path, "w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=list(rows[0]))
        w.writeheader()
        w.writerows(rows)
    return path


def write_results_md(rows, scenario: str, md_path: Path, dropout, tier: str):
    BEGIN_MARK, END_MARK = bench_marks(tier)
    tier_desc = ("realistic execution (Gazebo + Nav2 + AMCL, RTF≈1)" if tier == "realistic"
                 else "idealized execution (gridsim: 0.5 m grid, 1 cell/tick, prioritized MAPF)")
    header = (
        f"{BEGIN_MARK}\n"
        f"_Last run: {datetime.now():%Y-%m-%d %H:%M} · scenario `{Path(scenario).name}` "
        f"· {tier_desc}"
        + (f" · dropout injected: {dropout[0]} killed at {dropout[1]}s, revived {dropout[2]}s later"
           if dropout else "") + "_\n\n"
        "| Tier | Strategy | Planner | Robots | Completed | Wall (s) | Throughput (tasks/min) "
        "| Mean makespan (s) | Recoveries | Mean recovery (s) |\n"
        "|---|---|---|---|---|---|---|---|---|---|\n")
    for r in rows:
        header += (f"| {r.get('tier', 'realistic')} | {r['strategy']} "
                   f"| {r.get('planner', '')} | {r['robots']} "
                   f"| {r['completed']}/{r['orders']} "
                   f"| {r['wall_s']} | {r['throughput_per_min']} | {r['makespan_mean_s']} "
                   f"| {r['recoveries']} | {r['recovery_mean_s']} |\n")
    header += f"\n{END_MARK}"

    text = md_path.read_text() if md_path.exists() else "# Results\n"
    if BEGIN_MARK in text and END_MARK in text:
        pre = text.split(BEGIN_MARK)[0]
        post = text.split(END_MARK)[1]
        text = pre + header + post
    else:
        text += f"\n\n## Latest benchmark run ({tier} tier)\n\n" + header + "\n"
    md_path.write_text(text)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--scenario", default=str(ROOT / "benchmarks/scenarios/peak_hour.yaml"))
    ap.add_argument("--strategies", default="greedy,hungarian,auction")
    ap.add_argument("--robots", default="2", help="comma-separated fleet sizes")
    ap.add_argument("--orders", type=int, default=5, help="orders per run")
    ap.add_argument("--rate", type=float, default=4.0, help="scenario playback speedup")
    ap.add_argument("--tier", default="realistic", choices=["realistic", "idealized"],
                    help="realistic = Gazebo+Nav2 (slow, small N); idealized = gridsim (fast, any N)")
    ap.add_argument("--planners", default="prioritized",
                    help="idealized tier only: comma-separated (prioritized,pibt)")
    ap.add_argument("--dropout", default=None,
                    help="NAME:KILL_AFTER_S:REVIVE_AFTER_S — inject one robot dropout per run")
    ap.add_argument("--out", default=str(ROOT / "benchmarks/results"))
    ap.add_argument("--results-md", default=str(ROOT / "docs/results.md"))
    args = ap.parse_args()

    dropout = None
    if args.dropout:
        name, kill_s, revive_s = args.dropout.split(":")
        dropout = (name, float(kill_s), float(revive_s))

    rows = []
    if args.tier == "idealized":
        subprocess.run(["go", "build", "-o", "bin/gridsim", "./cmd/gridsim"],
                       cwd=ROOT / "fleet_manager", check=True)
        schedule = dump_schedule(args.scenario, args.orders)
        for robots in [int(n) for n in args.robots.split(",")]:
            for planner in args.planners.split(","):
                for alloc in args.strategies.split(","):
                    rows.append(run_one_idealized(schedule, alloc.strip(), robots,
                                                  args.orders, args.rate, planner.strip()))
    else:
        for robots in [int(n) for n in args.robots.split(",")]:
            for alloc in args.strategies.split(","):
                rows.append(run_one(args.scenario, alloc.strip(), robots,
                                    args.orders, args.rate, dropout))

    csv_path = write_csv(rows, Path(args.out))
    write_results_md(rows, args.scenario, Path(args.results_md), dropout, args.tier)
    print(f"\n[bench] CSV: {csv_path}")
    print(f"[bench] table updated in {args.results_md}")
    print("\n{:<11} {:<12} {:>6} {:>10} {:>8} {:>12} {:>13}".format(
        "strategy", "planner", "robots", "completed", "wall_s", "tasks/min", "makespan_s"))
    for r in rows:
        print("{:<11} {:<12} {:>6} {:>10} {:>8} {:>12} {:>13}".format(
            r["strategy"], r.get("planner", ""), r["robots"],
            f"{r['completed']}/{r['orders']}",
            r["wall_s"], r["throughput_per_min"], r["makespan_mean_s"]))


if __name__ == "__main__":
    main()
